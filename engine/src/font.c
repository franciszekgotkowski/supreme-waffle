static inline void handle_spacing(mstr ms, Font* font){
    assert(font);
    assert(ms);

    ms = nextWord(ms);
    switch (ms[1]) {
        case 'C':
            font->fontSpacing = CELL;
            break;
        case 'M':
            font->fontSpacing = MONOSPACED;
            break;
        case 'P':
            font->fontSpacing = PROPORTIONAL;
            break;
    }
}

